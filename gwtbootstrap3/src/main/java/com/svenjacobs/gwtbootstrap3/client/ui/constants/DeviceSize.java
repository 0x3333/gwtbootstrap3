package com.svenjacobs.gwtbootstrap3.client.ui.constants;

/**
 * Enum for the different type of device sizes that are supported.
 *
 * @author Joshua Godi
 */
public enum DeviceSize {
    PRINT, /* Print Devices */
    XS, /* Phones <768px */
    SM, /* Tablets >=768px */
    MD, /* Desktops >=992px */
    LG /* Desktops >=1200px */
}
